        // Mortage Eligibility Calculator========================================================
        $('.mortage').click(function() {
            let salary = parseInt($('#salary').val()) * 0.7;
            let other_emi = parseInt($('#other_emi').val());
            let tenure = parseInt($("#tenure").val());
            let propVal = parseInt($("#propVal").val());

            let income, property;
            // Income Eligibility
            switch (true) {
                case (tenure == 5):
                    income = ((salary - other_emi) / 2027) * 1e5;
                    break;
                case (tenure == 10):
                    income = ((salary - other_emi) / 1213) * 1e5;
                    break;
                case (tenure == 15):
                    income = ((salary - other_emi) / 956) * 1e5;
                    break;
                default:
                    alert(salary);
            }

            if ($('#resident').is(":checked")) {
                property = propVal * 0.65;

            } else if ($('#commer').is(":checked")) {
                property = propVal * 0.55;
            }

            $('#income').text(income.toFixed(0));
            $('#property').text(property.toFixed(0));

        });
        // Mortage Eligibility Calculator========================================================
